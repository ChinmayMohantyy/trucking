    //     ]);
    // }

    public function register(Request $request)
    {
        $user = new User();
        $user->first_name = $request->first_name;
        $user->last_name = $request->last_name;
        $user->email = $request->email;
        $user->password = bcrypt($request->password);
        $user->phone = $request->phone;
        $user->survey = $request->survey;
        $user->user_type = $request->user_type;
        $user->organisation_type = $request->bond;
        $user->company_name = $request->company_name;
        $user->business_types = $request->business_type;
        $user->mc_ff_mx = $request->mc_ff_mx.$request->mc_ff;
        $user->dot = $request->dot;
        $user->post_load = $request->post_load;
        $user->tms_use = $request->tms_use;
        $user->tc_agreed = $request->agree;
        $user->save();
        return redirect (url('/find-load'));

    }
}
